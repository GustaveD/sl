#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int		i;

	i = 0;
	while (i == 0 || (i > 0 && str[i - 1] != '\0'))
	{
		if (str[i] == (char)c)
			return ((char*)(&str[i]));
		i++;
	}
	return (0);
}
