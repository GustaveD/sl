#include "libft.h"

size_t		ft_stricpy(char *dest, const char *src)
{
	int i;

	i = 0;
	while (i == 0 || (i > 0 && src[i - 1] != '\0'))
	{
		dest[i] = src[i];
		i++;
	}
	return (i - 1);
}
